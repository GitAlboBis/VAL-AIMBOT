def _install_dispatch_side_effect(
    mock: MagicMock, raise_latch: List[bool]
) -> None:
    """
    Wire every relevant ``kmNet.*`` attribute on ``mock`` to consult a
    shared raise-latch.

    The latch (``raise_latch[0]``) is mutated by the test body around
    each driver method invocation: ``True`` while a method whose
    ``should_raise`` flag is set is in flight, ``False`` otherwise. Every
    ``kmNet.*`` mock attribute's ``side_effect`` is set to a closure that
    raises ``RuntimeError`` when the latch is True and returns ``0``
    (mirroring the vendor's success code) otherwise.

    Using a single shared latch — rather than per-attribute side effects
    — means the test does not need to predict which attribute a given
    driver method will reach. That keeps the exception-injection logic
    aligned with the property's intent (which is *what gets called*, not
    *which call raised*).
    """

    def _side_effect(*args: Any, **kwargs: Any) -> int:
        if raise_latch[0]:
            raise RuntimeError("injected_mock_failure")
        # Vendor functions return 0 on success; mirroring keeps the
        # mock's behaviour aligned with the real ``kmNet.*`` contract.
        return 0

    for attr_name in _ALL_DISPATCH_ATTRS:
        getattr(mock, attr_name).side_effect = _side_effect


def _reset_dispatch_call_history(mock: MagicMock) -> None:
    """
    Clear ``call_count`` / ``call_args_list`` on every dispatch attribute
    while preserving the ``side_effect`` configured by
    :func:`_install_dispatch_side_effect`.

    ``mock.reset_mock()`` (without ``side_effect=True``) preserves the
    side_effect by design, which is exactly what we need: the
    exception-injection wiring must survive the reset that clears any
    incidental ``kmNet.*`` calls made during driver construction.
    """
    for attr_name in _ALL_DISPATCH_ATTRS:
        getattr(mock, attr_name).reset_mock()


def _sum_call_counts(mock: MagicMock, attrs: Iterable[str]) -> int:
    """Total invocation count across the given ``mock.*`` attributes."""
    return sum(getattr(mock, a).call_count for a in attrs)


def _invoke_driver_method(
    driver: KmBoxNetDriver,
    method_name: str,
    args: Tuple[Any, ...],
) -> None:
    """
    Dispatch a single tagged method call against the driver.

    ``send_move`` re-raises UDP transmission failures per Req 1.8, so a
    raising mock attribute will propagate out of :meth:`move` /
    :meth:`move_relative`. Every other method (``click``,
    ``click_button``, ``mouse_down``, ``mouse_up``, ``key_press``,
    ``scroll``) catches and logs the exception internally per the
    existing public-API contract. We absorb the propagated ``move``
    exception here so the property loop continues; the property's
    correctness is independent of whether any individual call raised.
    """
    try:
        if method_name == "move":
            driver.move(*args)
        elif method_name == "click":
            driver.click(*args)
        elif method_name == "click_button":
            driver.click_button(*args)
        elif method_name == "mouse_down":
            driver.mouse_down(*args)
        elif method_name == "mouse_up":
            driver.mouse_up(*args)
        elif method_name == "key_press":
            driver.key_press(*args)
        elif method_name == "scroll":
            driver.scroll(*args)
        else:  # pragma: no cover - guarded by the strategy enumeration
            raise AssertionError(f"unknown method: {method_name!r}")
    except RuntimeError as e:
        # Req 1.8 — ``send_move`` re-raises UDP transmission failures.
        # The ``injected_mock_failure`` we surface inside the
        # ``raise_latch`` is the test-side analogue, so we tolerate it
        # here. Anything else is a real bug and should propagate.
        if str(e) != "injected_mock_failure":
            raise


