# Move components in [-100, 100] per the task plan: well inside the
# Req 1.4 valid-input window so every move passes the no-op gate and
# actually reaches ``_dispatch_call``. (The Req 1.4 invalid-input branch
# is covered by Property 3 in ``test_kmbox_invalid_move.py``.)
_MOVE_FLOATS = st.floats(
    min_value=-100.0,
    max_value=100.0,
    allow_nan=False,
    allow_infinity=False,
)


def _method_call(
    name: str,
    args_strategy: st.SearchStrategy[Tuple[Any, ...]],
) -> st.SearchStrategy[Tuple[str, Tuple[Any, ...], bool]]:
    """Build a tagged-tuple strategy ``(name, args, should_raise)``.

    The ``should_raise`` boolean is consulted by the test body to flip
    the shared exception-injection latch around the driver method
    invocation, exercising Req 4.6 ("drop only that command, do not fall
    back").
    """
    return st.tuples(st.just(name), args_strategy, st.booleans())


# Each method-call shape is generated as a tagged tuple so the test body
# can ``match`` on the first element. Argument tuples are wrapped with
# ``st.tuples`` so the args entry is always a tuple (uniform shape across
# methods).
_METHOD_CALL_STRATEGY = st.one_of(
    # ``move(x, y)`` — Req 1.3 valid-input branch, exercises the
    # ``"move"`` key in both dispatch tables.
    _method_call("move", st.tuples(_MOVE_FLOATS, _MOVE_FLOATS)),
    # ``click()`` — exercises the ``send_click`` path which issues
    # ``_dispatch_call("left", 1)`` then ``_dispatch_call("left", 0)``.
    # Per the task plan: "Don't include rate-limit-affected click()
    # repetition issues" — we drive it through the same tables but the
    # driver's ``target_cps`` rate limiter may legitimately drop calls
    # within the window. Either outcome is fine for the property: a
    # dropped click contributes nothing to either side of the count
    # comparison.
    _method_call("click", st.just(())),
    # ``click_button(button)`` — full identifier set covers both
    # ``press_release`` (string) and edge (integer) branches of
    # ``_BUTTON_EDGE``.
    _method_call(
        "click_button", st.tuples(st.sampled_from(_CLICK_BUTTON_IDS))
    ),
    # ``mouse_down(button)`` / ``mouse_up(button)`` — full identifier
    # set per Req 4.3a.
    _method_call(
        "mouse_down", st.tuples(st.sampled_from(_HOLD_BUTTON_IDS))
    ),
    _method_call(
        "mouse_up", st.tuples(st.sampled_from(_HOLD_BUTTON_IDS))
    ),
    # ``key_press(vk_code, hold_ms)`` — VK 0x41 (A) is in the
    # ``_vk_to_hid`` table; hold_ms=0 keeps the deterministic Req 4.8
    # hold collapsed to ``time.sleep(0)``.
    _method_call("key_press", st.tuples(st.just(0x41), st.just(0))),
    # ``scroll(amount)`` — small integer wheel deltas.
    _method_call("scroll", st.tuples(st.integers(min_value=-10, max_value=10))),
)

_METHOD_CALL_LIST_STRATEGY = st.lists(
    _METHOD_CALL_STRATEGY, min_size=0, max_size=30
)


