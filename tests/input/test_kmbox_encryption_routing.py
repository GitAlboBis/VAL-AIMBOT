@pytest.mark.unit
@_PROPERTY_SETTINGS
@given(
    use_encryption=st.booleans(),
    method_calls=_METHOD_CALL_LIST_STRATEGY,
)
def test_encryption_flag_governs_send_site_routing(
    use_encryption: bool,
    method_calls: List[Tuple[str, Tuple[Any, ...], bool]],
) -> None:
    """
    Validates Req 4.1 / 4.2 / 4.4 / 4.5 / 4.6.

    For any sequence of public driver-method invocations — including
    individual invocations whose underlying ``kmNet.*`` call raises an
    exception — the encryption flag dictates which attribute table is
    consulted at every send-site:

    * ``use_encryption=True``  ⇒  zero invocations of any plaintext
      ``kmNet.{move, left, right, middle, wheel, keydown, keyup}``.
    * ``use_encryption=False`` ⇒  zero invocations of any encrypted
      ``kmNet.enc_{move, left, right, middle, wheel, keydown, keyup}``.

    Per-call exceptions are injected via a shared raise-latch toggled
    around each method invocation; the driver's internal exception
    handling — log-and-swallow for the public-API methods, re-raise from
    ``send_move`` per Req 1.8 — is exercised in both branches and MUST
    NOT cause a fall-back to the opposite table (Req 4.6).
    """
    # ----- Install kmNet mock so ``_connect()`` lands in CONNECTED ---------
    # The driver is constructed inside the patched ``threading.Thread``
    # context below so the connect worker runs synchronously; with the
    # mock returning ``0`` immediately the bounded ``queue.get(timeout=5.0)``
    # in ``_connect()`` resolves on the first poll and the driver
    # transitions to CONNECTED.
    original_kmnet = kmbox_net_driver.kmNet
    mock_kmnet = MagicMock(name="kmNet")
    mock_kmnet.init.return_value = 0

    # The shared raise-latch consulted by every dispatch-attribute
    # ``side_effect``. Single-element list so the closure can mutate it
    # without ``nonlocal``.
    raise_latch: List[bool] = [False]
    _install_dispatch_side_effect(mock_kmnet, raise_latch)

    kmbox_net_driver.kmNet = mock_kmnet
    try:
        # Patch ``threading.Thread`` and ``time.sleep`` for the entire
        # test (construction + method loop). The thread substitution is
        # safe during construction because ``kmNet.init`` returns ``0``
        # via the mock, so the synchronous fake worker completes inline.
        # The ``time.sleep`` substitution suppresses the click hold and
        # keyboard hold; the rate-limit logic in ``click`` reads
        # ``time.monotonic`` (left untouched) and may legitimately drop
        # clicks within the ``1.0 / target_cps`` window — that is fine
        # for this property because a dropped click contributes nothing
        # to either side of the count comparison.
        with patch(
            "input.kmbox_net_driver.threading.Thread",
            new=_make_thread_factory(),
        ), patch(
            "input.kmbox_net_driver.time.sleep",
            new=lambda *_a, **_kw: None,
        ):
            driver = KmBoxNetDriver(
                ip=_TEST_IP,
                port=_TEST_PORT,
                uuid=_TEST_UUID,
                use_encryption=use_encryption,
                # ``target_cps=1000`` keeps the click rate-limit window
                # at 1 ms. Most clicks within a short Hypothesis-driven
                # sequence will still drop, but that is irrelevant to
                # the property — see commentary above.
                target_cps=1000.0,
            )

            # Precondition: with the mocked ``kmNet.init`` returning 0
            # the driver MUST land in CONNECTED, otherwise
            # ``_dispatch_call`` short-circuits and every subsequent
            # call is silently dropped (which would make the property
            # vacuously true and hide a routing regression).
            assert driver.connection_status is ConnectionStatus.CONNECTED, (
                "fixture precondition failed: driver did not reach "
                "CONNECTED state with mocked kmNet.init returning 0"
            )

            # Discard any incidental ``kmNet.*`` calls made during
            # construction (none expected today, but the reset is cheap
            # insurance against future ``__init__`` evolution). The
            # ``side_effect`` wiring is preserved.
            _reset_dispatch_call_history(mock_kmnet)

            # ----- Drive the method-call sequence ----------------------
            for method_name, args, should_raise in method_calls:
                # Req 4.6 — toggle the latch only around this single
                # invocation so an injected failure cannot bleed across
                # method boundaries.
                raise_latch[0] = should_raise
                try:
                    _invoke_driver_method(driver, method_name, args)
                finally:
                    raise_latch[0] = False

        # ----- Property assertion ----------------------------------------
        plaintext_calls = _sum_call_counts(mock_kmnet, _PLAINTEXT_ATTRS)
        encrypted_calls = _sum_call_counts(mock_kmnet, _ENCRYPTED_ATTRS)

        if use_encryption:
            # Req 4.1 / 4.2 / 4.4 / 4.5 — encrypted mode emits ZERO
            # plaintext calls; Req 4.6 — a raised exception inside an
            # encrypted call MUST NOT cause the driver to fall back to
            # the plaintext table on subsequent invocations.
            assert plaintext_calls == 0, (
                "Property 1 violated: use_encryption=True emitted "
                f"{plaintext_calls} plaintext kmNet call(s) across "
                f"{len(method_calls)} method invocation(s); "
                f"per-attribute counts: "
                + ", ".join(
                    f"{a}={getattr(mock_kmnet, a).call_count}"
                    for a in _PLAINTEXT_ATTRS
                )
                + f"; method_calls={method_calls!r}"
            )
        else:
            # Symmetric assertion: plaintext mode emits ZERO encrypted
            # calls.
            assert encrypted_calls == 0, (
                "Property 1 violated: use_encryption=False emitted "
                f"{encrypted_calls} encrypted kmNet call(s) across "
                f"{len(method_calls)} method invocation(s); "
                f"per-attribute counts: "
                + ", ".join(
                    f"{a}={getattr(mock_kmnet, a).call_count}"
                    for a in _ENCRYPTED_ATTRS
                )
                + f"; method_calls={method_calls!r}"
            )
    finally:
        # Restore the module-level ``kmNet`` binding regardless of test
        # outcome so subsequent tests in the same session are not
        # contaminated by this test's mock.
        kmbox_net_driver.kmNet = original_kmnet
