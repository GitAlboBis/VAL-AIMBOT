# Synchronous thread fake
# ---------------------------------------------------------------------------


class _SyncFakeThread:
    """
    Drop-in for :class:`threading.Thread` whose ``start()`` runs the target
    inline.

    Two driver paths spawn ``threading.Thread`` instances:

    * :meth:`KmBoxNetDriver._connect` — bounds ``kmNet.init`` against the
      5-second cap by harvesting the worker result through a
      ``queue.Queue``. With a mock that returns ``0`` immediately the
      worker completes inline and the bounded ``queue.get`` succeeds on
      the first attempt.
    * :meth:`KmBoxNetDriver.click` — admits a click and spawns a worker
      thread that calls :meth:`send_click`, which in turn issues two
      ``_dispatch_call("left", ...)`` invocations (the down and up edges).
      Running this worker inline lets the property observe the dispatch
      attempts in the same call frame as the ``click()`` invocation.

    ``is_alive()`` returns ``False`` permanently so the
    ``BaseMouse.click_thread.is_alive()`` short-circuit in
    :meth:`KmBoxNetDriver.click` never blocks subsequent admissions on a
    phantom in-flight worker. ``join()`` is a no-op so any future caller
    that joins on the thread does not deadlock.
    """

    __slots__ = ("_target", "_args", "_kwargs")

    def __init__(
        self,
        *args: Any,
        target: Any = None,
        args_kw: Any = None,
        kwargs: Any = None,
        name: Any = None,
        daemon: Any = None,
        **_extra: Any,
    ) -> None:
        # ``threading.Thread`` accepts target/args/kwargs both positionally
        # and by keyword; we only ever construct the fake by keyword in the
        # driver, but accept the positional form too for robustness.
        self._target = target
        # ``args`` is the conventional Thread keyword name for the target's
        # positional tuple; the keyword-only ``args_kw`` above is defensive
        # against the (unlikely) case of ``threading.Thread(args=...)``
        # being called positionally with the same name as the
        # ``*args`` capture above.
        self._args: Tuple[Any, ...] = tuple(args_kw) if args_kw else ()
        self._kwargs = dict(kwargs) if kwargs else {}

    def start(self) -> None:
        if self._target is not None:
            self._target(*self._args, **self._kwargs)

    def is_alive(self) -> bool:
        return False

    def join(self, timeout: Any = None) -> None:  # pragma: no cover - defensive
        return None


def _make_thread_factory():
    """
    Return a callable usable as a ``threading.Thread`` replacement.

    ``threading.Thread`` is most often constructed by keyword
    (``Thread(target=..., args=..., daemon=True, name=...)``). The driver
    code uses exactly that shape. We forward the conventional ``args``
    keyword into ``_SyncFakeThread`` under the alias ``args_kw`` to avoid
    colliding with the ``*args`` capture in :class:`_SyncFakeThread`'s
    constructor (the alias lets us keep the parameter name unambiguous
    while still accepting the standard Thread call-shape from outside).
    """

    def _factory(*args: Any, **kwargs: Any) -> _SyncFakeThread:
        target = kwargs.pop("target", None)
        thread_args = kwargs.pop("args", ())
        thread_kwargs = kwargs.pop("kwargs", None)
        # Discard ``name`` / ``daemon`` / any other Thread-only kwargs.
        kwargs.pop("name", None)
        kwargs.pop("daemon", None)
        return _SyncFakeThread(
            target=target,
            args_kw=thread_args,
            kwargs=thread_kwargs,
        )

    return _factory


