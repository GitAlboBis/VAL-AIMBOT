"""
Property test — Task 4.5 of spec ``kmbox-net-integration``.

# Feature: kmbox-net-integration, Property 1: Encryption flag governs
#   every send-site routing.

**Property 1: Encryption flag governs every send-site routing**

    *For any* sequence of public-method calls on :class:`KmBoxNetDriver`
    (``move`` / ``click`` / ``click_button`` / ``mouse_down`` /
    ``mouse_up`` / ``key_press`` / ``scroll``) — including sequences in
    which one or more individual ``kmNet.*`` invocations raise an
    exception — when the driver is constructed with
    ``use_encryption=True`` the test SHALL observe **zero** invocations of
    any plaintext ``kmNet.{move, left, right, middle, wheel, keydown,
    keyup}`` function across the entire sequence. Conversely, when the
    driver is constructed with ``use_encryption=False`` the test SHALL
    observe **zero** invocations of any encrypted
    ``kmNet.enc_{move, left, right, middle, wheel, keydown, keyup}``
    function.

**Validates: Requirements 4.1, 4.2, 4.4, 4.5, 4.6**

Implementation notes
--------------------
The driver routes every send-site through ``KmBoxNetDriver._dispatch_call``,
which selects between ``_PLAINTEXT_FN`` and ``_ENCRYPTED_FN`` based on
``use_encryption`` (see ``input/kmbox_net_driver.py`` and ``design.md`` §
"``_dispatch_call`` — encrypted-vs-plaintext canonical table"). Property 1
is the formal statement that this single-table consultation is the only
place where the encryption flag is read at runtime: there is no fall-back
path from encrypted to plaintext on per-call failure (Req 4.6) and no
stray ``kmNet.left`` (or any other plaintext attribute) call leaks
through while encryption is on.

Per-call exception injection (Req 4.6)
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
Each method-call entry generated by Hypothesis carries a ``should_raise``
flag. Before invoking the method we set a shared latch consulted by every
``kmNet.*`` mock attribute's ``side_effect``; while the latch is True any
``kmNet.*`` call raises ``RuntimeError("injected_mock_failure")``. After
the method returns we clear the latch. The driver's per-method exception
handlers (``send_click`` / ``click_button`` / ``mouse_down`` /
``mouse_up`` / ``key_press`` / ``scroll`` all log-and-swallow per the
existing contract; ``send_move`` re-raises per Req 1.8) decide whether to
propagate or absorb the failure — the property holds either way because a
dropped (or partially-dropped) command contributes nothing to either side
of the count comparison.

Threading and timing patches
~~~~~~~~~~~~~~~~~~~~~~~~~~~~
* ``threading.Thread`` is replaced with a synchronous fake (``_SyncFakeThread``)
  so :meth:`KmBoxNetDriver.click`'s worker (which calls ``send_click`` and
  therefore the dispatch path under test) executes inline rather than on a
  real daemon. The fake also tolerates being constructed before
  ``__init__`` returns (the ``_connect()`` worker uses the same primitive
  to bound ``kmNet.init`` against the 5-second cap); with a mock that
  returns ``0`` immediately the worker completes inline and the bounded
  ``queue.get`` succeeds on the first attempt.
* ``time.sleep`` is replaced with a no-op so neither the click hold (in
  ``send_click`` / ``click_button``) nor the keyboard hold (in
  ``key_press``) wastes wall-clock time. The driver's rate-limit reads
  ``time.monotonic`` (not ``time.sleep``) and is left untouched; clicks
  within the ``1.0 / target_cps`` window may legitimately drop, which is
  fine — a dropped click contributes nothing to either side of the count
  comparison.

Method-call generator
~~~~~~~~~~~~~~~~~~~~~
The strategy is ``lists(one_of(...))`` over a tagged-tuple shape
``(method_name, args_tuple, should_raise)``. Move arguments are drawn
from ``[-100.0, 100.0]`` so they pass the Req 1.4 finite/range gate
without exercising the no-op branch (which is covered by Property 3).
Button identifiers cover the full accepted set on each method —
``"left"`` / ``"right"`` / ``"middle"`` and the DD-compatible integer
codes — so the property exercises both ``_BUTTON_EDGE`` and
``_BUTTON_HOLD`` resolution. ``key_press`` uses ``vk_code=0x41`` (the
``A`` key, present in :meth:`KmBoxNetDriver._vk_to_hid`) and
``hold_ms=0`` so the deterministic Req 4.8 hold collapses to a no-op
``time.sleep(0)`` regardless of whether ``time.sleep`` is patched.
"""

