"""Integration tests for the Audit_Document invariants.

Task 1.3 of the ``single-config-streamlining`` spec verifies that
``.kiro/specs/single-config-streamlining/audit.md`` satisfies the three
invariants required by Requirements 1.1, 1.2, 1.3, 1.4 and 1.10:

* **Coverage invariant** — every ``.py`` file in the workspace (excluding
  ``.archive/``, ``htmlcov/``, ``.pytest_cache/``, ``__pycache__/`` and
  ``.kiro/``) appears exactly once in the summary table under the
  ``## Tabella riepilogativa`` section of ``audit.md``.
* **Partition invariant** — every row in the summary table is classified
  as either ``KEEP`` or ``REMOVE``; no ``EVALUATE`` residual remains
  (Req 1.7).
* **Consistency invariant** — no file classified ``KEEP`` imports a file
  classified ``REMOVE``. This is a *post-refactoring* invariant: the
  Audit_Document explicitly acknowledges (under the "Invariante di
  coerenza (post-refactoring)" section) that a small set of files
  currently import REMOVE modules and will be cleaned up by later tasks
  (2.2, 3.2, 4.2, 5.2, 12.1, 13.1, 14.1). The test therefore asserts
  that the current set of KEEP→REMOVE violations is a *subset* of the
  acknowledged set, so the invariant strengthens monotonically as
  cleanup tasks run.
"""

from __future__ import annotations

