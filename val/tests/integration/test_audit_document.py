# --- Tests ----------------------------------------------------------------

@pytest.mark.integration
def test_coverage_invariant(
    summary_table: Dict[str, str], workspace_py_files: List[Path]
) -> None:
    """Every workspace ``.py`` file appears exactly once in the table.

    The test tolerates two benign asymmetries:

    * ``.py`` files introduced by later tasks of this same spec
      (see ``_SPEC_ARTIFACT_FILES``) legitimately exist on disk without
      appearing in the audit; they are excluded from the check.
    * After fases 2-3 run, REMOVE files will be physically deleted; the
      audit still lists them (that is exactly what ``audit.md`` is for).
      So extra rows whose classification is REMOVE are tolerated.
    """
    workspace_rel = {
        _workspace_rel(p)
        for p in workspace_py_files
        if _workspace_rel(p) not in _SPEC_ARTIFACT_FILES
    }
    table_rel = set(summary_table.keys())

    missing = sorted(workspace_rel - table_rel)
    extra = sorted(
        rel
        for rel in (table_rel - workspace_rel)
        if summary_table[rel] != "REMOVE"
    )

    assert not missing, (
        "Coverage invariant violated: the following workspace .py files "
        "are not classified in audit.md#Tabella riepilogativa:\n  - "
        + "\n  - ".join(missing)
    )
    assert not extra, (
        "Coverage invariant violated: audit.md classifies KEEP files "
        "that do not exist in the workspace (or live under an excluded "
        "folder):\n  - " + "\n  - ".join(extra)
    )

    # Exactly once: the parser already dedups by key, so reparse the raw
    # table lines to catch accidental duplicates that would collide.
    raw = _AUDIT_PATH.read_text(encoding="utf-8")
    header_match = _TABLE_HEADER_RE.search(raw)
    start = header_match.end()
    next_heading = re.search(r"^\s*##\s", raw[start:], re.MULTILINE)
    end = start + next_heading.start() if next_heading else len(raw)
    files_in_order = [
        m.group("file").strip()
        for line in raw[start:end].splitlines()
        for m in [_TABLE_ROW_RE.match(line)]
        if m and m.group("cls").strip().lower() != "classification"
    ]
    duplicates = sorted({f for f in files_in_order if files_in_order.count(f) > 1})
    assert not duplicates, (
        "Coverage invariant violated: files listed more than once in the "
        "summary table:\n  - " + "\n  - ".join(duplicates)
    )


@pytest.mark.integration
def test_partition_invariant(summary_table: Dict[str, str]) -> None:
    """Only KEEP and REMOVE classifications; no EVALUATE residuals."""
    allowed = {"KEEP", "REMOVE"}
    bad = {
        path: cls
        for path, cls in summary_table.items()
        if cls not in allowed
    }
    assert not bad, (
        "Partition invariant violated: the summary table must classify "
        "every file as KEEP or REMOVE (task 1.2 resolves all EVALUATE). "
        "Non-conforming rows:\n  - "
        + "\n  - ".join(f"{k}: {v}" for k, v in sorted(bad.items()))
    )


@pytest.mark.integration
def test_consistency_invariant_keep_does_not_import_removed(
    summary_table: Dict[str, str], workspace_py_files: List[Path]
) -> None:
    """No KEEP file imports a REMOVE file (post-refactoring invariant).

    Until fases 2-3 run, a bounded, acknowledged set of KEEP files still
    imports REMOVE modules (see audit.md "Invariante di coerenza
    (post-refactoring)"). The test asserts that the *current* set of
    violating KEEP files is a subset of that acknowledged set, so the
    invariant strengthens monotonically as cleanup tasks 2.2, 3.2, 4.2,
    5.2, 12.1, 13.1 and 14.1 complete.
    """
    keep_files = {
        p for p, cls in summary_table.items() if cls == "KEEP"
    }
    remove_files = {
        p for p, cls in summary_table.items() if cls == "REMOVE"
    }
    workspace_py_rel = {_workspace_rel(p) for p in workspace_py_files}
    module_map = _build_module_to_file_map(workspace_py_files)

    # gui/*.py are permitted to still reference REMOVE modules pre-task 14.1.
    acknowledged = set(_ACKNOWLEDGED_PRE_REFACTOR_VIOLATORS)
    acknowledged |= {
        rel for rel in workspace_py_rel
        if rel.startswith("gui/") and rel in keep_files
    }

    violations: Dict[str, Set[str]] = {}
    for rel in sorted(keep_files):
        abs_path = _WORKSPACE / rel
        # File might be classified KEEP in the audit but missing from disk
        # (shouldn't happen after coverage test passes, but be defensive).
        if not abs_path.is_file():
            continue
        package = _package_of(rel)
        for module, name, level in _imports_in_file(abs_path):
            resolved = _resolve_to_workspace_file(
                module, name, level, package, module_map
            )
            if resolved is not None and resolved in remove_files:
                violations.setdefault(rel, set()).add(resolved)

    unexpected = {
        keep: targets
        for keep, targets in violations.items()
        if keep not in acknowledged
    }
    if unexpected:
        lines = [
            f"  - {keep} imports REMOVE: {sorted(targets)}"
            for keep, targets in sorted(unexpected.items())
        ]
        pytest.fail(
            "Consistency invariant violated: KEEP files importing REMOVE "
            "modules outside the acknowledged pre-refactoring set. Either "
            "clean the import or add the file to the audit.md "
            "'Invariante di coerenza (post-refactoring)' section.\n"
            + "\n".join(lines)
        )
