# --- Audit parsing --------------------------------------------------------

_TABLE_HEADER_RE = re.compile(r"^\s*## Tabella riepilogativa\s*$", re.MULTILINE)
# Matches a row like: | `path/to/file.py` | `KEEP` | reason text |
_TABLE_ROW_RE = re.compile(
    r"^\|\s*`(?P<file>[^`]+)`\s*\|\s*`(?P<cls>[^`]+)`\s*\|\s*(?P<reason>.*?)\s*\|\s*$"
)


def _parse_summary_table(audit_text: str) -> Dict[str, str]:
    """Return a mapping ``file -> classification`` from the summary table.

    The summary table lives under the ``## Tabella riepilogativa``
    heading and ends at the next ``##`` heading. Only rows whose first
    cell is a backticked path and second cell is a backticked
    classification are considered (header and separator rows are
    skipped automatically).
    """
    header_match = _TABLE_HEADER_RE.search(audit_text)
    assert header_match is not None, (
        "audit.md is missing the '## Tabella riepilogativa' section; "
        "task 1.1 must produce it and task 1.2 must keep it."
    )
    start = header_match.end()
    # Stop at the next '## ' heading (not deeper levels).
    next_heading = re.search(r"^\s*##\s", audit_text[start:], re.MULTILINE)
    end = start + next_heading.start() if next_heading else len(audit_text)
    section = audit_text[start:end]

    rows: Dict[str, str] = {}
    for line in section.splitlines():
        m = _TABLE_ROW_RE.match(line)
        if not m:
            continue
        file_path = m.group("file").strip()
        classification = m.group("cls").strip()
        # Skip a stray header row if someone wrapped headers in backticks.
        if classification.lower() == "classification":
            continue
        rows[file_path] = classification
    assert rows, (
        "The summary table under '## Tabella riepilogativa' is empty; "
        "task 1.1 must populate it with one row per workspace .py file."
    )
    return rows


# --- Workspace scan -------------------------------------------------------

def _iter_workspace_python_files() -> Iterable[Path]:
    """Yield every ``.py`` file in the workspace, skipping excluded dirs."""
    for path in _WORKSPACE.rglob("*.py"):
        rel_parts = path.relative_to(_WORKSPACE).parts
        if any(part in _EXCLUDED_DIRS for part in rel_parts):
            continue
        yield path


def _workspace_rel(path: Path) -> str:
    """Return a POSIX workspace-relative path (matches audit.md format)."""
    return path.relative_to(_WORKSPACE).as_posix()


# --- Import resolution ----------------------------------------------------

def _build_module_to_file_map(
    py_files: Iterable[Path],
) -> Dict[str, str]:
    """Map ``a.b.c`` dotted module names to workspace-relative file paths.

    ``pkg/__init__.py`` maps to ``pkg``; ``pkg/mod.py`` maps to
    ``pkg.mod``. This mirrors Python's import semantics for top-level
    packages rooted at the workspace root.
    """
    module_map: Dict[str, str] = {}
    for path in py_files:
        rel = _workspace_rel(path)
        parts = rel.split("/")
        if parts[-1] == "__init__.py":
            mod = ".".join(parts[:-1])
        else:
            mod = ".".join(parts[:-1] + [parts[-1][:-3]])
        if mod:
            module_map[mod] = rel
    return module_map


def _package_of(rel_path: str) -> str:
    """Return the dotted package name that hosts ``rel_path``.

    For ``pkg/mod.py`` returns ``pkg``; for ``pkg/sub/__init__.py``
    returns ``pkg.sub``; for top-level ``mod.py`` returns ``""``.
    """
    parts = rel_path.split("/")
    if parts[-1] == "__init__.py":
        return ".".join(parts[:-1])
    return ".".join(parts[:-1]) if len(parts) > 1 else ""


def _resolve_relative(
    package: str, module: str | None, level: int
) -> str | None:
    """Resolve a relative ``from`` import to an absolute dotted module."""
    if level <= 0:
        return module or None
    pkg_parts = package.split(".") if package else []
    # level=1 means "current package"; level=2 means "parent", etc.
    if level > len(pkg_parts) + 1:
        return None
    base = pkg_parts[: len(pkg_parts) - (level - 1)] if level > 1 else pkg_parts
    if module:
        return ".".join([*base, module]) if base else module
    return ".".join(base) if base else None


def _imports_in_file(path: Path) -> List[Tuple[str, str | None, int]]:
    """Return a list of ``(module_or_None, name_or_None, level)`` tuples.

    * ``import a.b`` → ``("a.b", None, 0)``
    * ``from a.b import c`` → ``("a.b", "c", 0)``
    * ``from . import x`` inside package ``pkg`` → ``(None, "x", 1)``
    * ``from .mod import x`` inside ``pkg`` → ``("mod", "x", 1)``
    """
    try:
        source = path.read_text(encoding="utf-8")
    except (OSError, UnicodeDecodeError):
        return []
    try:
        tree = ast.parse(source, filename=str(path))
    except SyntaxError:
        return []

    results: List[Tuple[str, str | None, int]] = []
    for node in ast.walk(tree):
        if isinstance(node, ast.Import):
            for alias in node.names:
                results.append((alias.name, None, 0))
        elif isinstance(node, ast.ImportFrom):
            module = node.module  # may be None for "from . import x"
            level = node.level or 0
            for alias in node.names:
                results.append((module, alias.name, level))
    return results


def _resolve_to_workspace_file(
    module: str | None,
    name: str | None,
    level: int,
    package: str,
    module_map: Dict[str, str],
) -> str | None:
    """Resolve one AST import record to a workspace-relative file path.

    Returns ``None`` for third-party imports or unresolved relatives.
    If both ``module`` and ``module.name`` resolve, ``module.name`` is
    preferred because ``from pkg import submod`` imports the submodule.
    """
    abs_module = _resolve_relative(package, module, level)

    candidates: List[str] = []
    if abs_module and name:
        candidates.append(f"{abs_module}.{name}")
    if abs_module:
        candidates.append(abs_module)
    if name and not abs_module and level == 0:
        # Plain ``import name`` (AST: Import, level is always 0)
        candidates.append(name)

    for cand in candidates:
        if cand in module_map:
            return module_map[cand]
    return None


